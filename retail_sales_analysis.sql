-- PERFORMANCE ANALYSIS

/* ANALYSING THE YEARLY PERFORMANCE OF PRODUCTS BY COMPARING THEIR SALES TO BOTH 
   THE AVERAGE SALES PERFORMANCE OF THE PRODUCT AND THE PREVIOUS YEAR'S SALES*/
with yearly_product_sales as 
                             (
                             select
                             year(s.order_date) as Years,
                             p.product_name,
                             sum(s.sales_amount) as Current_Sales
                             from gold.fact_sales s
                             left join gold.dim_products p
                             on s.product_key = p.product_key
                             where year(s.order_date) is not null
                             group by year(s.order_date),  p.product_name)
select *,
lag(Current_Sales) over (partition by product_name order by Years) as Pv_Yrs_Sales, -- YEAR OVER YEAR ANALYSIS
Current_Sales - lag(Current_Sales) over (partition by product_name order by Years) as Sale_diff,
case 
when Current_Sales - lag(Current_Sales) over (partition by product_name order by Years) > 0 then 'Sales Increased'
when Current_Sales - lag(Current_Sales) over (partition by product_name order by Years) < 0 then 'Sales Decreased'
else 'No change'
End as Sale_Change,
avg(Current_Sales) over (partition by product_name) as Avg_Sales,
Current_Sales - avg(Current_Sales) over (partition by product_name) as Avg_Diff,
case 
when Current_Sales - avg(Current_Sales) over (partition by product_name) > 0 then 'Above Avg'
when Current_Sales - avg(Current_Sales) over (partition by product_name) < 0 then 'Below Avg'
else 'Avg'
End as Avg_Change
from yearly_product_sales
order by product_name, Years



