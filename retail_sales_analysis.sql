-- DATA SEGMENTATION

-- SEGMENTING PRODUCTS INTO COST RANGES AND CHECKING HOW MANY PRODUCTS FALL INTO EACH CATEGORY.
with product_segments as (
                        select product_key,
                        product_name,
                        cost,
                        case 
                        when cost < 100 then 'Below 100'
                        when cost between 100 and 500 then '100 - 500'
                        when cost between 500 and 1000 then '500 - 1000'
                        else 'Above 1000'
                        End as Cost_Range
                        from gold.dim_products )
select Cost_Range,
count(product_key) as No_of_Products
from product_segments
group by Cost_Range
order by No_of_Products desc


/*Group customers into three segments based on their spending behavior:
	- VIP: Customers with at least 12 months of history and spending more than €5,000.
	- Regular: Customers with at least 12 months of history but spending €5,000 or less.
	- New: Customers with a lifespan less than 12 months.
And find the total number of customers by each group */
with customer_spending as (
                            select c.customer_key,
                            sum(s.sales_amount) as Total_Sale,
                            min(s.order_date) as first_order,
                            max(s.order_date) as last_order,
                            datediff(month, min(s.order_date), max(s.order_date)) as order_history_Months
                            from gold.fact_sales s
                            left join gold.dim_customers c
                            on s.customer_key = c.customer_key
                            group by c.customer_key
                            ),
customer_segments as(
                            select customer_key,
                            Total_Sale,
                            order_history_Months,
                            case 
                            when Total_Sale > 5000 and order_history_Months >= 12  then 'VIP'
                            when Total_Sale <= 5000 and order_history_Months >= 12  then 'REGULAR'
                            else 'NEW'
                            end as customer_segments
                            from customer_spending
                            )
select customer_segments,
count(customer_key) as Total_Customers
from customer_segments
group by customer_segments
order by Total_Customers Desc
                            


/*
===============================================================================
Customer Report
===============================================================================
Purpose:
    - This report consolidates key customer metrics and behaviors

Highlights:
    1. Gathers essential fields such as names, ages, and transaction details.
	2. Segments customers into categories (VIP, Regular, New) and age groups.
    3. Aggregates customer-level metrics:
	   - total orders
	   - total sales
	   - total quantity purchased
	   - total products
	   - lifespan (in months)
    4. Calculates valuable KPIs:
	    - recency (months since last order)
		- average order value
		- average monthly spend
===============================================================================
*/

create view gold.report_customers as 

with base_query as (
-- 1) Base Query: Retrieves core columns from tables
                            select 
                            s.order_number,
                            s.product_key,
                            s.order_date,
                            s.sales_amount,
                            s.quantity,
                            c.customer_key,
                            c.customer_number,
                            concat(c.first_name, ' ' , c.last_name) as Cust_Name,
                            datediff(year, c.birthdate, getdate()) as Age
                            from gold.fact_sales s
                            left join gold.dim_customers c
                            on c.customer_key = s.customer_key
                            where order_date is not null
                            ),
customer_aggregation as (
-- 2) Customer Aggregations: Summarizes key metrics at the customer level
                            select customer_key,
                            customer_number,
                            Cust_Name,
                            Age,
                            count(distinct(order_number)) as Total_Orders,
                            SUM(sales_amount) AS total_sales,
                            SUM(quantity) AS total_quantity,
                            COUNT(DISTINCT product_key) AS total_products,
                            MAX(order_date) AS last_order_date,
                            DATEDIFF(month, MIN(order_date), MAX(order_date)) AS lifespan
                            from base_query
                            group by customer_key, customer_number, Cust_Name, Age
                            )
select 
customer_key,
customer_number,
Cust_Name,
Age,
case
when Age < 20 then 'BELOW 20'
when Age between 20 and 29 then '20-29'
when Age between 30 and 39 then '30-39'
when Age between 40 and 49 then '40-49'
else '50 AND ABOVE'
end as Age_Groups,
case
when Total_Sales > 5000 and lifespan >= 12  then 'VIP'
when Total_Sales <= 5000 and lifespan >= 12  then 'REGULAR'
else 'NEW'
end as customer_segments,
last_order_date,
datediff(month, last_order_date, getdate()) as Recency,
Total_Orders,
total_sales,
case 
when Total_Orders = 0 then 0
else total_sales /Total_Orders
end as Avg_Order_Value, -- AVO
total_quantity,
total_products,
lifespan,
case 
when lifespan = 0 then total_sales
else total_sales/lifespan 
end as Avg_Monthly_Spend
from customer_aggregation

select * from gold.report_customers



/*
===============================================================================
Product Report
===============================================================================
Purpose:
    - This report consolidates key product metrics and behaviors.

Highlights:
    1. Gathers essential fields such as product name, category, subcategory, and cost.
    2. Segments products by revenue to identify High-Performers, Mid-Range, or Low-Performers.
    3. Aggregates product-level metrics:
       - total orders
       - total sales
       - total quantity sold
       - total customers (unique)
       - lifespan (in months)
    4. Calculates valuable KPIs:
       - recency (months since last sale)
       - average order revenue (AOR)
       - average monthly revenue
===============================================================================
*/

CREATE VIEW gold.report_products AS

WITH base_query AS (
/*---------------------------------------------------------------------------
1) Base Query: Retrieves core columns from fact_sales and dim_products
---------------------------------------------------------------------------*/
    SELECT
	    f.order_number,
        f.order_date,
		f.customer_key,
        f.sales_amount,
        f.quantity,
        p.product_key,
        p.product_name,
        p.category,
        p.subcategory,
        p.cost
    FROM gold.fact_sales f
    LEFT JOIN gold.dim_products p
        ON f.product_key = p.product_key
    WHERE order_date IS NOT NULL  -- only consider valid sales dates
),

product_aggregations AS (
/*---------------------------------------------------------------------------
2) Product Aggregations: Summarizes key metrics at the product level
---------------------------------------------------------------------------*/
SELECT
    product_key,
    product_name,
    category,
    subcategory,
    cost,
    DATEDIFF(MONTH, MIN(order_date), MAX(order_date)) AS lifespan,
    MAX(order_date) AS last_sale_date,
    COUNT(DISTINCT order_number) AS total_orders,
	COUNT(DISTINCT customer_key) AS total_customers,
    SUM(sales_amount) AS total_sales,
    SUM(quantity) AS total_quantity,
	ROUND(AVG(CAST(sales_amount AS FLOAT) / NULLIF(quantity, 0)),1) AS avg_selling_price
FROM base_query

GROUP BY
    product_key,
    product_name,
    category,
    subcategory,
    cost
)

/*---------------------------------------------------------------------------
  3) Final Query: Combines all product results into one output
---------------------------------------------------------------------------*/
SELECT 
	product_key,
	product_name,
	category,
	subcategory,
	cost,
	last_sale_date,
	DATEDIFF(MONTH, last_sale_date, GETDATE()) AS recency_in_months,
	CASE
		WHEN total_sales > 50000 THEN 'High-Performer'
		WHEN total_sales >= 10000 THEN 'Mid-Range'
		ELSE 'Low-Performer'
	END AS product_segment,
	lifespan,
	total_orders,
	total_sales,
	total_quantity,
	total_customers,
	avg_selling_price,
	-- Average Order Revenue (AOR)
	CASE 
		WHEN total_orders = 0 THEN 0
		ELSE total_sales / total_orders
	END AS avg_order_revenue,

	-- Average Monthly Revenue
	CASE
		WHEN lifespan = 0 THEN total_sales
		ELSE total_sales / lifespan
	END AS avg_monthly_revenue

FROM product_aggregations 

select * from gold.report_products